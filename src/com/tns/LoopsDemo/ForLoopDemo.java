package com.tns.LoopsDemo;

    // Program for Forloop
public class ForLoopDemo {

	public static void main(String[] args) {
		// Using forloop
		
		for(int i = 1; i<=100; i++) //(int i = 100; i<=1;i--)
        {
            System.out.print("Value of i:");
            System.out.println(i);
        }
    }


	}


